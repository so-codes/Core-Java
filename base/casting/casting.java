package base.casting;

public class casting {
    public static void main(String[] args){

        // int to double

        int sleet = 10;
        double yeet = sleet;  // automatic casting

        System.out.println(sleet);
        System.out.println(yeet);

        System.out.print("\n");

        // double to int

        double dou = 5.5;
        int inn = (int) dou;  // manual casting

        System.out.println(dou);
        System.out.println(inn);
    }
}
